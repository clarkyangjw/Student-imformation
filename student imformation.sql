CREATE TABLE a2Employees(
    empID       NUMBER(5)       PRIMARY KEY,  
    firstName   VARCHAR2(30)    NOT NULL,
    lastName    VARCHAR2(30)    NOT NULL,
    prefix      VARCHAR2(5),
