--Create views
--i. Student Transcript that contains all courses, marks for each student
CREATE OR REPLACE VIEW a2VIEW1 AS
    SELECT st.firstname||' '||st.lastname AS Name,
            st.studentId AS StudentID,
            t.termcode AS Term,
            c.coursecode AS courseCode,
            j1.marks
        FROM a2term t JOIN a2sections s ON t.termCode=s.termCode
                JOIN a2courses c ON c.courseCode=s.coursecode
                JOIN a2JNC_STUDENTS_SECTION j1 ON j1.sectionid=s.sectionid
                JOIN a2students st ON j1.studentId=st.studentId;

SELECT * FROM a2VIEW1
    WHERE StudentID LIKE upper('&Student_ID%')
    ORDER BY Name,Term;

--ii. Class list for students enrolled in particular course section
CREATE OR REPLACE VIEW a2View2 AS
    SELECT DISTINCT s.firstName || ' ' || s.lastName AS Name,
            c.courseCode AS Course,
            sc.sectionLetter AS Section
        FROM a2students s JOIN a2JNC_PROG_STUDENTS j1 ON j1.studentID=s.studentID
                JOIN a2Programs p ON p.progCode=j1.progCode
                JOIN a2JNC_PROG_COURSES j2 ON j2.progCode=p.progCode
                JOIN a2Courses c ON c.courseCode=j2.courseCode
                JOIN a2Sections sc ON sc.courseCode = c.courseCode;

SELECT * FROM a2View2 
    WHERE upper(course) LIKE upper('&courseCode%')
        AND upper(section) LIKE upper('&SectionLetter%')
    ORDER BY name;

--iii. Average mark of each section and professor who instructed
CREATE OR REPLACE VIEW a2VIEW3 AS
    SELECT AVG(j1.marks) AS AVG_Grade,
            s.sectionId AS SectionID,
            s.profId AS Prof#,
            e.firstname AS Prof_FName,
            e.lastname AS Prof_LName
        FROM a2JNC_STUDENTS_SECTION j1 JOIN a2sections s ON s.sectionId=j1.sectionId
                JOIN a2Professors p ON s.profId=p.empId
                JOIN a2employees e ON e.empId=p.empId
        GROUP BY s.sectionId,s.profId,e.firstname,e.lastname;
        
        
SELECT * FROM a2VIEW3
    WHERE AVG_Grade IS NOT NULL
    ORDER BY AVG_Grade;

--iv. An output of all the courses required for each program sorted by program name and the term required.

CREATE OR REPLACE VIEW a2VIEW4 AS
    SELECT progCode AS Program, 
            courseCode AS Course,
            (CASE
                WHEN courseCode LIKE '___1__'
                THEN 1
                WHEN courseCode LIKE '___2__'
                THEN 2
                WHEN courseCode LIKE '___3__'
                THEN 3
                WHEN courseCode LIKE '___4__'
                THEN 4
                WHEN courseCode LIKE '___5__'
                THEN 5
                WHEN courseCode LIKE '___6__'
                THEN 6
                WHEN courseCode LIKE '___7__'
                THEN 7
                WHEN courseCode LIKE '___8__'
                THEN 8
            END) AS CoursetermRequired
        FROM a2JNC_Prog_Courses;


SELECT * FROM a2VIEW4        
    ORDER BY Program, coursetermrequired;


--v. Term specific list of which profs are teaching which courses and how many students are enrolled in each course

CREATE OR REPLACE VIEW a2VIEW5 AS
SELECT t.termCode,
        t.termName,
        s.sectionId,
        e.firstname,
        count(j1.studentId) AS studentcount,
        c.coursecode,
        c.courseName
    FROM a2term t JOIN a2sections s ON s.termcode=t.termcode
            JOIN a2professors p ON s.profId=p.empId
            JOIN a2employees e ON p.empId=e.empId
            JOIN a2courses c ON c.courseCode=s.courseCode
            JOIN a2JNC_STUDENTS_SECTION j1 ON j1.sectionId=s.sectionId
    GROUP BY t.termCode,t.termName,e.firstname,c.coursecode,c.courseName,s.sectionId;


SELECT * FROM a2VIEW5
    WHERE upper(firstname) NOT IN ('UNKOWN')
    ORDER BY termCode;

